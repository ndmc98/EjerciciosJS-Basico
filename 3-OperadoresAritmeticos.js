//SUMA

let a = 3;
let b = 2;
let z = a + b;

console.log("Resultado de la suma: " + z);

//RESTA

z = a - b;
console.log("Resultado de la resta: " + z);

//MULTIPLICACIÓN

z = a * b;
console.log("Resultado de la multiplicación: " + z);

//DIVISIÓN
z = a / b;
console.log("Resultado de la división: " + z);

//MÓDULO - Residuo o resto
z = a % b;
console.log("Resultado del módulo o residuo: " + z);

//EXPONENTE
z = a ** b;
console.log("Resultado del exponente: " + z);

//INCREMENTO
z = a ++;
console.log("Resultado del incremento: " + a);

//DECREMENTO
z = a --;
console.log("Resultado del decremento: " + a);

/*Incremento y decremento*/

//Variables
let x = 3, y = 4;
let sum = x + y;

//INCREMENTO
//Preincremento (++ antes de la variable)
sum = ++x;
console.log(x)
console.log(sum)

//Posincremento (++ después de la variable)
sum = y++;
console.log(y)
console.log(sum)

//DECREMENTO
//Predecremento (-- antes de la variable)
sum = --x;
console.log(x)
console.log(sum)

//Posdecremento (-- después de la variable)
sum = y--;
console.log(y)
console.log(sum)